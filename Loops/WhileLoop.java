package Loops;

public class WhileLoop {
    public static void main(String[] args){
        // While loop
        // print numbers from 1 to 100 using while loop
        int i =1, j=1;
        while(i<=10 && j<=10){
            System.out.println(" i = "+ i + " and j= "+j);
            i++;
            j++;
        }
    }
}
