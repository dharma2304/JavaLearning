package Operators.TernaryOperator;

public class TernaryOperator {
    public static void main(String[] args){
        // Ternary operator: Its an shorthand syntax of if-else statement.
        // syntax: datatype <variableName> = (condition) ? <expression if its true> : <expression if its false>;
        boolean x = (5>4) ? true: false;
        System.out.println(x);

        int y;
        y= (10 >9)? 10: 9;
        System.out.println(y);
    }
}
